package com.bridgelabz.oop.utility
import java.util.*

class Utility {
    var sc = Scanner(System.`in`)

    fun integerInput(): Int {
        return sc.nextInt()
    }

    fun doubleInput(): Double {
        return sc.nextDouble()
    }

    fun stringInput(): String? {
        return sc.next()
    }

    fun longInput(): Int {
        return sc.nextInt()
    }
}